export async function editImage(base64Image: string, mimeType: string, prompt: string, ref64?: string, refMime?: string): Promise<string> {
    const parts: any[] = [{ inlineData: { data: base64Image, mimeType } }];
    if (ref64 && refMime) parts.push({ inlineData: { data: ref64, mimeType: refMime } });
    parts.push({ text: `${prompt} - Blend naturally.` });

    return generateWithRetry(async () => {
        const response = await ai.models.generateContent({
            model: 'gemini-2.5-flash-image',
            contents: { parts },
            config: { temperature: 0.5, safetySettings },
        });
        const part = response.candidates?.[0]?.content?.parts?.find((p: any) => p.inlineData);
        if (part) return part.inlineData.data;
        throw new Error("Edit failed.");
    });
}
