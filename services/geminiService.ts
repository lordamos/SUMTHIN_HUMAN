export async function improveText(text: string, style: string): Promise<string> {
    let instruction = '';
    switch (style) {
        case 'rephrase-gpt': instruction = `Rewrite the text. Provide 5 bulleted versions with different structures and tones.`; break;
        case 'reword': instruction = 'Reword while keeping the meaning identical.'; break;
        case 'shorten': instruction = 'Summarize and tighten the phrasing.'; break;
        case 'lengthen': instruction = 'Expand with more detail and descriptive language.'; break;
        case 'simplify': instruction = 'Use simpler vocabulary and shorter sentences.'; break;
        case 'readability': instruction = 'Organize for maximum clarity and ease of reading.'; break;
        case 'fix': default: instruction = 'Fix all spelling and grammar mistakes.'; break;
    }

    const prompt = `${instruction}\n\nText:\n${text}`;
    return generateWithRetry(async () => {
        const response = await ai.models.generateContent({
            model: 'gemini-3-flash-preview',
            contents: prompt,
            config: { temperature: 0.5, safetySettings },
        });
        return response.text.trim();
    });
}

export async function generateSpeech(text: string, voiceName: string = 'Zephyr'): Promise<string> {
    return generateWithRetry(async () => {
        const response = await ai.models.generateContent({
            model: "gemini-2.5-flash-preview-tts",
            contents: [{ parts: [{ text }] }],
            config: {
                responseModalities: [Modality.AUDIO],
                speechConfig: {
                    voiceConfig: { prebuiltVoiceConfig: { voiceName } },
                },
            },
        });
        const base64Audio = response.candidates?.[0]?.content?.parts?.[0]?.inlineData?.data;
        if (!base64Audio) throw new Error("TTS failed.");
        return base64Audio;
    });
}

